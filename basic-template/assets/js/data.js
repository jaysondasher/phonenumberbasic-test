// Helper function to find listing by phone number
function findListingByPhone(phoneNumber) {
    const cleanPhone = cleanPhoneNumber(phoneNumber);
    return phoneListings.find(listing => cleanPhoneNumber(listing.phone) === cleanPhone);
}

// Helper function to clean phone number for comparison
function cleanPhoneNumber(phone) {
    return phone.replace(/\D/g, '');
}

// Helper function to format phone number for display
function formatPhoneNumber(phone) {
    const cleaned = cleanPhoneNumber(phone);
    if (cleaned.length === 10) {
        return `${cleaned.slice(0,3)}-${cleaned.slice(3,6)}-${cleaned.slice(6)}`;
    }
    return phone;
}